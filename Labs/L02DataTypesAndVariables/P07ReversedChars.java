package Labs.L02DataTypesAndVariables;

import java.util.Scanner;

public class P07ReversedChars {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        String symbol1 = scanner.nextLine();
        String symbol2 = scanner.nextLine();
        String symbol3 = scanner.nextLine();
        System.out.printf("%s %s %s ", symbol3, symbol2, symbol1);
    }
}
